public class Card {
	
	private String description;
	private int label;
	
	public void setDescription(String s) {
		description = s;
	}
	
	public String getDescription() {
		return description;
	}
	
	public void setLabel(int i) {
		label = i;
	}
	
	public int getLabel() {
		return label;
	}
	
}
